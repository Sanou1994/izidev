                            <h2><b>Thilogne</b> et son contexte</h2>
						</div>
                        <br><h3>A- Contexte géographique</h3><br>
						<p style="text-align: justify;">Le Fouta est situé au Nord-Est du Sénégal, entre la Mauritanie au Nord et le Mali à l’Est. Cette sous-région, partie du Sahel, est principalement habitée par les Toucouleurs et les Soninkés, qui constituent la grande majorité de l’immigration sénégalaise en France.Enclavée du reste du Sénégal par son éloignement et par un manque d’infrastructures de communication, de pôles économiques, cette partie du Sahel dispose, comme seules ressources, des produits de l’agriculture et de l’élevage (sa population était à plus de 95% des agriculteurs et des éleveurs avant la grande sécheresse de 1973).</p>
                        <br><br><h3>B- Contexte écologique et socio-économique</h3><br>
                        <p style="text-align: justify;">Durant vingt longues années, de 1973 à 1993, cette sous-région a connu la sécheresse la plus grande et la plus dévastatrice de son histoire, réduisant à néant l’agriculture et l’élevage du terroir. Sans pluie ni pâturage, les agriculteurs et les éleveurs du Fouta ont été privés de leurs principales sources de subsistance alors qu’elles leur assuraient autrefois l’autosuffisance alimentaire. Handicapés par cette sécheresse, les hommes en âge de travailler ont été contraints à quitter leurs villages pour la capitale, partant à la recherche d’un hypothétique emploi. Certains ont prolongé leur émigration vers d’autres capitales africaines ; d’autres sont encore allés plus loin, en Europe et aux Etats Unis.</p>
						<p style="text-align: justify;">Tous sont partis avec l’espoir de « trouver du travail » afin de subvenir aux besoins élémentaires de leurs familles restées aux villages. Ce "long voyage des gens du fleuve" a malheureusement laissé la plupart des villages à l’abandon, vidés de leurs forces vives. Ne pouvant ou ne voulant pas quitter leur terre natale, les femmes, les enfants et les vieillards sont devenus les seuls habitants. Alors que les aléas climatiques continuaient de sévir, les conditions de vie sont devenues de plus en plus difficiles voire insupportables. Les conditions écologiques et économiques désastreuses ont aggravé les problèmes de malnutrition, de santé et d’hygiène, augmentant le taux de mortalité surtout chez les personnes les plus fragiles, nourrissons et personnes de plus de soixante ans. C’est dans cet environnement hostile et défavorisé que les Thilognois et d’autres villageois ont vécu toutes ces années de sécheresse.</p>
